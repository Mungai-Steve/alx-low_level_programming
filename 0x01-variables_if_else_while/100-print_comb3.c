#include <stdio.h>

/**
 * main - program starts
 *
 * Return: program ends
 */

int main(void)

{
	int a, b;

	for (a = '0'; a <= '8'; a++)

	for (b = '1'; b <= '9'; b++)
	
	if (a != b)
{
	putchar(a);
	putchar(b);


{
	putchar(',');
	putchar(' ');

}
}

	if (a != '8' && b != '9')
{
	putchar('\n');

}
	return (0);

}
