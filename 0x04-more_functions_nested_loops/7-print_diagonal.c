#include "main.h"
/**
 * @n: integer
 * print_diagonal - print diagonal line
 */

void print_diagonal(int n)

{

	int i, j;

	if (n <= 0)
	{
	for (i = 0; i < n; i++)
	{
	for (j = 0; j < n; j++)
	{
	_putchar(32);
	{
	_putchar(92);
	if (j == n - 1)
	{
	continue;
	}
	}
	}
	}
	{
	_putchar('\n');
	}
	{
	_putchar('\n');
}
}
}
